package io.openems.edge.controller.ess.sohcycle;

import org.osgi.service.metatype.annotations.AttributeDefinition;
import org.osgi.service.metatype.annotations.ObjectClassDefinition;

@ObjectClassDefinition(//
		name = Config.CONTROLLER_ESS_SO_H_CYCLE, //
		description = "Tests the SoH cycle of a connected ESS")
public @interface Config {

	static final String CONTROLLER_ESS_SO_H_CYCLE = "Controller Ess SoH Cycle";

	@AttributeDefinition(name = "Component-ID", description = "Unique ID of this Component")
	String id() default "ctrlEssSohCycle0";

	@AttributeDefinition(name = "Ess-ID", description = "ID of Ess device.")
	String ess_id() default "ess0";

	@AttributeDefinition(name = "Ess target filter", description = "This is auto-generated by 'Ess-ID'.")
	String ess_target() default "(enabled=true)";

	@AttributeDefinition(name = "Alias", description = "Human-readable name of this Component; defaults to Component-ID")
	String alias() default "";

	@AttributeDefinition(name = "Is enabled?", description = "Is this Component enabled?")
	boolean enabled() default true;

	@AttributeDefinition(name = "Is running?", description = "Is the SoH cycle currently running?")
	boolean isRunning() default false;

	@AttributeDefinition(name = "Log Verbosity", description = "Sets the logging verbosity level.")
	LogVerbosity logVerbosity() default LogVerbosity.NONE;

	@AttributeDefinition(name = "Enable Reference Cycle", description = "Enables the reference cycle measurement. If disabled, the cycle skips directly to measurement cycle.")
	boolean referenceCycleEnabled() default false;

	String webconsole_configurationFactory_nameHint() default CONTROLLER_ESS_SO_H_CYCLE + " [{id}]";

}