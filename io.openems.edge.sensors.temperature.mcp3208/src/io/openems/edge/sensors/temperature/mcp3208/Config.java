import org.osgi.service.metatype.annotations.AttributeDefinition;
import org.osgi.service.metatype.annotations.ObjectClassDefinition;

import io.openems.edge.bridge.spi.api.Board;

@ObjectClassDefinition( //
        name = "Sensor Temperature MCP3208", //
        description = "Implements the MCP3208 temperature sensor.")
@interface Config {
    String service_pid();

    String id() default "temp0";

    boolean enabled() default true;

    @AttributeDefinition(name = "SPI-ID", description = "ID of SPI bridge.")
    String spi_id();

    @AttributeDefinition(name = "Board", description = "The board. default is ")
    String board() default "LEAFLET_1_00";

    @AttributeDefinition(name = "Position", description = "The position. default is POS_1")
    String position() default "POS_1";

    @AttributeDefinition(name = "SPI target filter", description = "This is auto-generated by 'SPI-ID'.")
    String spi_target() default "";

    String webconsole_configurationFactory_nameHint() default "Sensor Temperature MCP3208 [{id}]";
}